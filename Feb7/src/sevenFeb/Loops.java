package sevenFeb;

public class Loops
{
public static void main(String args[])   
{   
	 for (int i=0; i<=5; i++)
     {    	
  	 for (int j=5; j>=i;j--)
  	 {
  	System.out.print(" ");	 
  	 }
  	 
  	for (int a=0; a<=i; a++)
	
		{
			System.out.print("*");
		}
	
			System.out.println();
 
}
}
}